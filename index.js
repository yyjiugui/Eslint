// 定义变量必须使用
const a = 10
console.log(a)

// 结尾逗号
const b = { name: 'zs', age: 30 }
console.log(b)

const c = [1, 2, 3]
console.log(c)
// 使用单引号
const d = 'hello world'
console.log(d)
// 结束语句不要使用分号
const e = 100
console.log(e)

// 禁止出现多行空行
const f = 1000

console.log(f)

const g = 'hello world'
console.log(g)

